# PL/PGSQL

* Esta seção mostra passo a passo como usar o PL / pgSQL para desenvolver funções definidas pelo usuário e procedimentos armazenados do PostgreSQL;

* A linguagem procedural PL / pgSQL adiciona muitos elementos procedurais, por exemplo, estruturas de controle, loops e cálculos complexos para estender o SQL padrão. Ele permite que você desenvolva funções complexas e procedimentos armazenados no PostgreSQL que podem não ser possíveis usando SQL simples;

