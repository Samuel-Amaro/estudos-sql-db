* VISÃO GERAL DO POSTGRESQL PL/pgSQL

 - PL / pgSQL é uma linguagem de programação procedural para o sistema de banco de dados PostgreSQL;
 
 - PL / pgSQL permite estender a funcionalidade do servidor de banco de dados PostgreSQL criando objetos de servidor com lógica complexa;
 
 - PL/pgSQL foi Projetado para:
 
   - Crie funções definidas pelo usuário, procedimentos armazenados e gatilhos;
   
   - Estender SQL padrão por estruturas de controle de adição tais como if, case, e loopdeclarações;
   
   - Herdar todas as funções, operadores e tipos definidos pelo usuário;

* SQL é uma linguagem de consulta que permite consultar dados do banco de dados facilmente. No entanto, o PostgreSQL só pode executar instruções SQL individualmente;


   